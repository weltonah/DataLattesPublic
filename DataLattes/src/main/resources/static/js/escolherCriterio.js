
		function Formacao() {var x = document.getElementById("myCheckFormacao").checked;
		if (x) {
		    document.getElementById("txtFormacao").innerHTML="Ativado";
		    document.getElementById("FormacaoMisto").classList.remove("hidden");
		    document.getElementById("Formacao").style.backgroundColor = "#a5d6a7 ";
		}else{
		    document.getElementById("txtFormacao").innerHTML="Desativado";
		    document.getElementById("FormacaoMisto").classList.add("hidden");
		    document.getElementById("Formacao").style.backgroundColor = "#ffab91";
		}}	
