function formacaoMarkAll() {
	var x = document.getElementById("checkboxformacaoMarkAll").checked;
	var inputs = document.querySelectorAll('.formacao');
	if (x) {
		for (var i = 0; i < inputs.length; i++) {
			inputs[i].checked = true;
		}
	} else {
		for (var i = 0; i < inputs.length; i++) {
			inputs[i].checked = false;

		}
	}
}
